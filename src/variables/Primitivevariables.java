package variables;

public class Primitivevariables {
    public static void main(String[] args) {
        // type varName = value;
        //boolean can hold value: true, false
        boolean javaIsEasy = true;
        javaIsEasy = false;
        System.out.println(javaIsEasy);

        //byte range: -128....0....127
        byte smallNum = 50;
        System.out.println(smallNum);
        // numeric vars: byte, short, int, float, long, double
        short num1 = 450;
        int num2 = 1000000;
        float num3 = 5f;
        long num4 = 45l;
        double num5 = 12.4;
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(num5);

        // type varName = value;
        //char: holds single symbol-letter
        char letter = '$';

        System.out.println(letter);
        //create multiply int vars
        int var1 = 12,var2 = 13;
        double var3 = 44.5;
        /* multyline comment */
        /**
         * document comment
         * this boolean indicates the difficulty of java
         */


    }
}
